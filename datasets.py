class Nines(torch.utils.data.Dataset):
    def __init__(self, source, split, constant_angle=None):
        self.source = source
        self.split = split
        self.constant_angle = constant_angle

        if source == "mnist":
            self.dataset = self.get_mnist_nines(split)
        elif source == "simulated":
            self.dataset = [self.get_simulated_nine()] * 360




    def __len__(self):
        return len(self.dataset)



    def __getitem__(self, idx):
        theta = self.get_theta(idx)
        green = self.get_green(idx)

        return {
            'image': self.get_image(idx, theta, green),
            'theta': theta
        }



    def get_theta(self, idx):
        if self.constant_angle is not None:
            theta = self.constant_angle
        elif self.source == "mnist" and self.split == "train":
            theta = np.random.randint(360)
        elif self.source == "mnist" and self.split == "test":
            theta = idx % 360
        elif self.source == "simulated":
            theta = idx % 360

        return theta



    def get_green(self, idx):
        return np.random.randint(256)



    def get_image(self, idx, theta, green):
        image = self.dataset[idx]
        image = torchvision.transforms.functional.rotate(image, theta)
        image = torchvision.transforms.functional.to_tensor(image)
        image = torch.round(image)
        image = image.repeat(3,1,1)
        return image




    def get_mnist_nines(self, split):
        ''' Returns a list of PIL images '''
        mnist = torchvision.datasets.MNIST(root='./data', train=(split == "train"), download=True)
        dset = []
        for x in range(5):
            for im, num in mnist:
                if int(num) in [9]:#[2,3,4,5,7,9]:
                    dset.append(im)
        return dset




